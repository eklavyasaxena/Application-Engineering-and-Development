    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblFlights.getModel();
        dtm.setRowCount(0);
        
        for(Schedule schedule : airliner.getSchedules()){
            Object[] row = new Object[2];
            row[0] = schedule;
            row[1] = schedule.getAirplaneModelNo();
            
            dtm.addRow(row);
        }
        
    }

