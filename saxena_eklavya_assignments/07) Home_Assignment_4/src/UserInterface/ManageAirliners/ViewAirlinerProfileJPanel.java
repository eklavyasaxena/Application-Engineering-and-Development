    private void btnViewFlightDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewFlightDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblFlights.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select a Row from the TABLE to View Details", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Schedule schedule = (Schedule)tblFlights.getValueAt(selectedRow, 0);
            ViewFlightsJPanel panel = new ViewFlightsJPanel(userProcessContainer, schedule);
            userProcessContainer.add("ViewFlightsJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewFlightDetailsActionPerformed

    private void btnSearchFlightNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchFlightNoActionPerformed
        // TODO add your handling code here:
        if(txtSearchFlightNo.getText().equals("") || !(txtSearchFlightNo.getText().matches("[a-zA-Z0-9]+"))){
            JOptionPane.showMessageDialog(null,"Invalid Data");
        }
        else{
            Schedule result = airliner.searchFlight(txtSearchFlightNo.getText());
            if(result == null){
                JOptionPane.showMessageDialog(null, "Flight Number Does Not Exist", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                ViewFlightsJPanel panel = new ViewFlightsJPanel(userProcessContainer, result);
                userProcessContainer.add("ViewFlightsJPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                txtSearchFlightNo.setText("");
            }
        }
    }//GEN-LAST:event_btnSearchFlightNoActionPerformed

