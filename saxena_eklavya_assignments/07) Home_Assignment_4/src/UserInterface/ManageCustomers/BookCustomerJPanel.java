    private void btnBookTheFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookTheFlightActionPerformed
        // TODO add your handling code here:
        String customerID = txtCustomerID.getText();
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String eMail = txtEmail.getText();
        String phoneNum = txtPhoneNo.getText();
        String seatNum = txtSeatNumber.getText();
        
        if((customerID.equals("") || !(customerID.matches("[a-zA-Z0-9]+")))||
                (firstName.equals("") || !(firstName.matches("[a-zA-Z]+")))||
                (lastName.equals("") || !(lastName.matches("[a-zA-Z0-9]+")))||
                (eMail.equals("") || !(eMail.matches("[a-zA-Z0-9@.]+")))||
                (phoneNum.equals("") || !(phoneNum.matches("[0-9]+")))||
                (seatNum.equals("") || !(seatNum.matches("[a-zA-Z0-9]+")))){
            JOptionPane.showMessageDialog(null,"Invalid Data");
        }
        else{
            Customer customer = customerCat.addCustomer();
            customer.setCustomerID(customerID);
            customer.setFirstName(firstName);
            customer.setLastName(lastName);
            customer.setPhoneNum(phoneNum);
            customer.setSeatNum(seatNum);
            customer.seteMail(eMail);
            
            for(Seat s: schedule.getSeats()){
                if(seatNum.compareTo(s.getSeatNum())==0){
                    s.setCustomerID(customerID);
                    customer.setBookedSeatPrice(s.getSeatPrice());
                }
            }
            int i=1;
            for(Seat a : schedule.getSeats()){
                System.out.println(i+":SeatNum-->"+a.getSeatNum()+"\tSeatPrice-->"+a.getSeatPrice()+"\tCustomerID-->"+a.getCustomerID());
                i++;
            }
            
            JOptionPane.showMessageDialog(null, "Flight No: " + txtFlightNo.getText() + " with Seat No " 
                    + txtSeatNumber.getText() +" booked successfully. Please take a screenshot");
            
            txtCustomerID.setText("");
            txtFirstName.setText("");
            txtLastName.setText("");
            txtEmail.setText("");
            txtPhoneNo.setText("");
            txtSeatNumber.setText("");
            
            populateTable();
        }
    }//GEN-LAST:event_btnBookTheFlightActionPerformed

    private void btnAddSeatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSeatActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSeats.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select a Row from the TABLE to View Details", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Seat seat = (Seat)tblSeats.getValueAt(selectedRow, 0);
            txtSeatNumber.setText(seat.getSeatNum());
        }
    }//GEN-LAST:event_btnAddSeatActionPerformed


