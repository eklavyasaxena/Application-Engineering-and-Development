    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblSeats.getModel();
        dtm.setRowCount(0);
        
        for(Seat seat : schedule.getSeats()){
            if(seat.getCustomerID() == null){
                Object[] row = new Object[2];
                row[0] = seat;
                row[1] = seat.getSeatPrice();

                dtm.addRow(row);
            }
        }
    }

