    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try {    
            String username = txtUserName.getText().toLowerCase();
            UserAccount userAccount = new UserAccount();
            String password = userAccount.hash_SHA_512_SecurePassword(txtPassword.getText(), "COEXIST");
            
            System.out.println(username+"   "+password);
            String foundRole = "";
            for(UserAccount ua : userAccountDirectory.getUserList()){
                if(username.compareTo(ua.getUserName())==0&&password.compareTo(password)==0){
                    foundRole = ua.getRole();
                    userAccount = ua;
                }
            }
            if(foundRole.compareTo("System Admin")==0){
                
                System.out.println("Done");
                SystemAdminWorkAreaJPanel systemAdminWorkAreaJPanel = new SystemAdminWorkAreaJPanel(userAccount, userProcessContainer, personDirectory, userAccountDirectory);
                userProcessContainer.add("SystemAdminWorkAreaJPanel", systemAdminWorkAreaJPanel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);

                txtPassword.setText("");
                txtUserName.setText("");
            }
            
            else if(foundRole.compareTo("HR Admin")==0){
                
                System.out.println("Done");
                HRAdminWorkAreaJPanel panel = new HRAdminWorkAreaJPanel(userAccount, userProcessContainer, personDirectory, userAccountDirectory);
                userProcessContainer.add("HRAdminWorkAreaJPanel", panel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                System.out.println("Check");

                txtPassword.setText("");
                txtUserName.setText("");
            }        
            else{
                JOptionPane.showMessageDialog(null, "User Account Does Not Exist", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch (UnsupportedEncodingException ex) {
            Logger.getLogger(LoginScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed


