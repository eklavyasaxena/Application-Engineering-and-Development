    private void addjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addjButtonActionPerformed
        String name = diseasesjTextField.getText();
        if (name.isEmpty()) {
            // if selectedRow <0 means the row is not selected
            JOptionPane.showMessageDialog(null, "Please Insert Name of the Disease", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        System.out.println("AfterAddButton"+system);
        Disease dis = system.getDiseaseCatalog().addDisease();
        dis.setDiseaseName(name);
        
        populateTable();
    }//GEN-LAST:event_addjButtonActionPerformed

    private void managejButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_managejButton1ActionPerformed
        int selectRow=(Integer)diseasesjTable.getSelectedRow();
        if (selectRow < 0) {                                                           // if selectedRow <0 means the row is not selected
            JOptionPane.showMessageDialog(null, "please select any row from the first table", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Disease dis = (Disease) diseasesjTable.getValueAt(selectRow, 1);
        ManageVaccineDiseaseJPanel manageVaccineDiseaseJPanel = new ManageVaccineDiseaseJPanel(userProcessContainer, system,dis);
        userProcessContainer.add("ManageVaccineDiseaseJPanel", manageVaccineDiseaseJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_managejButton1ActionPerformed

    private void deletejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletejButtonActionPerformed
        int selectRow=(Integer)diseasesjTable.getSelectedRow();
        if (selectRow < 0) {                                                           // if selectedRow <0 means the row is not selected
            JOptionPane.showMessageDialog(null, "please select any row from the first table", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Disease dis = (Disease) diseasesjTable.getValueAt(selectRow, 1);
        system.getDiseaseCatalog().deleteDisease(dis);
        populateTable();
    }//GEN-LAST:event_deletejButtonActionPerformed


