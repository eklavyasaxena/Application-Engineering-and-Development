    private void populateTable() {
        System.out.println("Check"+system);
        DefaultTableModel model = (DefaultTableModel) diseasesjTable.getModel();
        int sno = 1;
        model.setRowCount(0);
        try{
            for (Disease dis: system.getDiseaseCatalog().getDiseasesList()) {
                        Object[] row = new Object[3];
                        row[0] = sno++;
                        row[1] = dis;
                        model.addRow(row);
            }
        }
            
        catch(Exception e){
                System.out.println("No data in table");
        }
                
    }

