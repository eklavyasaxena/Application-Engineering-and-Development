    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        
        // Get userName & password
        String userName = txtUserName.getText().toLowerCase();
        String inputPassword = String.valueOf(txtPassword.getPassword());
        String password = null;
        try {
            password = ecoSystem.getUserAccountDirectory().hash_SHA_512_SecurePassword(inputPassword, "COEXIST");
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

        
        //Step1: Check in the ecoSystem user account directory if you have the user
        UserAccount userAccount = ecoSystem.getUserAccountDirectory().authenticateUser(userName, password);
        Enterprise inEnterprise = null;
        Organization inOrganization = null;
        Network inNetwork = null;
        if (userAccount == null) {
            
            //Step2: Go inside each network to check each enterprise
            for (Network network : ecoSystem.getNetworkList()) {
                
                //Step 2-a: Check against each enterprise
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    userAccount = enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                    if (userAccount == null) {
                        
                        //Step3: Check against each organization inside that enterprise
                        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                            userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
                            if (userAccount != null) {
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                inNetwork = network;
                                break;
                            }
                        }
                        
                    } else {
                        inEnterprise = enterprise;
                        break;
                    }
                    
                    if (inOrganization != null) {
                        break;
                    }
                }
                if (inEnterprise != null) {
                    break;
                }
            }
        }

        
        if (userAccount == null) {
            JOptionPane.showMessageDialog(null, "Invalid Credentails!", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        } else {
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("workAreaJPanel", userAccount.getRole().createWorkAreaJPanel(userProcessContainer, userAccount, inOrganization, inEnterprise, inNetwork, ecoSystem));
            layout.next(userProcessContainer);
        }
        
        
        btnLogIn.setEnabled(false);
        btnLogOut.setEnabled(true);
        txtUserName.setEnabled(false);
        txtPassword.setEnabled(false);
        
    }//GEN-LAST:event_btnLogInActionPerformed

    private void btnLogOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogOutActionPerformed
        
        btnLogOut.setEnabled(false);
        txtUserName.setEnabled(true);
        txtPassword.setEnabled(true);
        btnLogIn.setEnabled(true);

        txtUserName.setText("");
        txtPassword.setText("");

        userProcessContainer.removeAll();
        
        JPanel blankJPanel = new JPanel();
        userProcessContainer.add("blank", blankJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
        dB4OUtil.storeSystem(ecoSystem);
    }//GEN-LAST:event_btnLogOutActionPerformed

