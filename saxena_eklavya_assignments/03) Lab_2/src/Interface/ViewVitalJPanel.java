    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tableOfVitalSigns.getModel();
        dtm.setRowCount(0);
        
        for (VitalSigns vs : vsh.getCollectionHistory()){
            Object row[] = new Object[2];
            row[0] = vs;
            row[1] = vs.getBloodPressure();
            dtm.addRow(row);
        }
    }

