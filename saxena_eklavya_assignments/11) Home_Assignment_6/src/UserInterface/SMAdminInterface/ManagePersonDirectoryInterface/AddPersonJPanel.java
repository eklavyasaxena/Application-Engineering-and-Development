    private void btnAddPersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPersonActionPerformed
        // TODO add your handling code here:
        String email = txtEmail.getText();
        String fname = txtFirstName.getText();
        String lname = txtLastName.getText();
        
        if((email.equals("") || !(email.matches("[0-9a-zA-Z@.]+")))||
                (fname.equals("") || !(fname.matches("[a-zA-Z]+")))||
                (lname.equals("") || !(lname.matches("[a-zA-Z]+")))){
            JOptionPane.showMessageDialog(null,"Invalid Data");
        }
        else{
            Person person = personDirectory.addPerson();
            person.setEmail(email);
            person.setFirstName(fname);
            person.setLastName(lname);
            
            JOptionPane.showMessageDialog(null, "Person Details Added Successfully");
            
            txtFirstName.setText("");
            txtEmail.setText("");
            txtLastName.setText("");
        }
    }//GEN-LAST:event_btnAddPersonActionPerformed

