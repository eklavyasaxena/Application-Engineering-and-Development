    private void btnSearchMarketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchMarketActionPerformed
        // TODO add your handling code here:
         if(txtMarketID.getText().equals("") || !(txtMarketID.getText().matches("[0-9A-Z]+"))){
            JOptionPane.showMessageDialog(null,"Invalid Data");
        }
        else{
            String id= txtMarketID.getText();
            Market market = marketCatalog.searchMarket(id);
        
            
            if(market == null){
                JOptionPane.showMessageDialog(null, "Market Does Not Exist", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                for(MarketOffer marketOffer : marketOfferCatalog.getMarketOfferList()){
                    if(marketOffer.getMarket().getMarketId().compareTo(id)==0){
                        SearchUpdateMarketPanel panel = new SearchUpdateMarketPanel(userProcessContainer, market, marketOffer);
                        userProcessContainer.add("SearchUpdateMarketPanel", panel);
                        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                        layout.next(userProcessContainer);

                        txtMarketID.setText("Enter Market ID Here");    
                    }
                }
            }
        }
    }//GEN-LAST:event_btnSearchMarketActionPerformed

