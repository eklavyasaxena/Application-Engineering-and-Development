    private void btnUpdateMarketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateMarketActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMarketList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select a Row from the TABLE to View Details", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Market market = (Market)tblMarketList.getValueAt(selectedRow, 0);
            for(MarketOffer marketOffer : marketOfferCatalog.getMarketOfferList()){
                if(marketOffer.getMarket().getMarketId().compareTo(market.getMarketId())==0){
                    SearchUpdateMarketPanel panel = new SearchUpdateMarketPanel(userProcessContainer, market, marketOffer);
                    userProcessContainer.add("SearchUpdateMarketPanel", panel);
                    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                    layout.next(userProcessContainer);
                }
            }
        }
       
    }//GEN-LAST:event_btnUpdateMarketActionPerformed

    private void btnDeleteMarketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteMarketActionPerformed
        // TODO add your handling code here:
          int row = tblMarketList.getSelectedRow();
        
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please Select a Row from the TABLE First", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to DELETE the Account?", "Warning", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION){
            Market m = (Market)tblMarketList.getValueAt(row, 0);
            marketCatalog.deleteMarket(m);
            populateTable();
        }
    }//GEN-LAST:event_btnDeleteMarketActionPerformed

