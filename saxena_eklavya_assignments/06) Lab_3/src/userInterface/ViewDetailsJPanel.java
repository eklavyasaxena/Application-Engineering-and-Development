    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if((txtRoutingNumber.getText().equals("") || !(txtRoutingNumber.getText().matches("[0-9]+")))||
                (txtAccountNumber.getText().equals("") || !(txtAccountNumber.getText().matches("[0-9]+")))||
                (txtBankName.getText().equals("") || !(txtBankName.getText().matches("[a-zA-Z]+")))){
            JOptionPane.showMessageDialog(null,"Invalid Data");
        }
        else{
            account.setRoutingNum(txtRoutingNumber.getText());
            account.setAccountNum(txtAccountNumber.getText());
            account.setBankName(txtBankName.getText());

            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);

            JOptionPane.showMessageDialog(null, "Account Updated Successfully");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

