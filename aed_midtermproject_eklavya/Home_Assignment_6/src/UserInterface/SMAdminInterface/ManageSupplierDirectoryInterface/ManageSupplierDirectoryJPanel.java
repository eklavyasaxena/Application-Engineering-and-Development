    private void btnUpdateSupplierDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateSupplierDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSupplierList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select a Row from the TABLE to View Details", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Supplier result = (Supplier)tblSupplierList.getValueAt(selectedRow, 0);
            SearchUpdateSupplierJPanel panel = new SearchUpdateSupplierJPanel(userProcessContainer, result, userAccountDirectory);
            userProcessContainer.add("SearchUpdatePersonJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnUpdateSupplierDetailsActionPerformed

    private void btnDeleteSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSupplierActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSupplierList.getSelectedRow();
        if(selectedRow >= 0){
            Supplier supplier = (Supplier)tblSupplierList.getValueAt(selectedRow, 0);
            
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to DELETE the Supplier?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                supplierDirectory.removeSupplier(supplier);
                populateTable();
            }           
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Select a Row from the TABLE", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteSupplierActionPerformed


