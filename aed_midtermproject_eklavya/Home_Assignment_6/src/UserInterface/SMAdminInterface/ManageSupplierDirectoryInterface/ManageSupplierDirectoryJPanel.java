    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblSupplierList.getModel();
        dtm.setRowCount(0);
        
        for(Supplier supplier : supplierDirectory.getSupplierlist()){
            Object[] row = new Object[2];
            row[0] = supplier;
            row[1] = supplier.getSupplierName();
            
            dtm.addRow(row);
        }
        
    }

