    private void btnSearchSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchSupplierActionPerformed
        // TODO add your handling code here:
        if(txtSearchSupplierID.getText().equals("") || !(txtSearchSupplierID.getText().matches("[0-9A-Z]+"))){
            JOptionPane.showMessageDialog(null,"Invalid Data");
        }
        else{
            Supplier result = supplierDirectory.searchSupplier(txtSearchSupplierID.getText());
            if(result == null){
                JOptionPane.showMessageDialog(null, "Supplier ID Does Not Exist", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                SearchUpdateSupplierJPanel panel = new SearchUpdateSupplierJPanel(userProcessContainer, result, userAccountDirectory);
                userProcessContainer.add("SearchUpdateSupplierJPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                
                txtSearchSupplierID.setText("Enter Supplier ID Here");
            }
        }
    }//GEN-LAST:event_btnSearchSupplierActionPerformed

